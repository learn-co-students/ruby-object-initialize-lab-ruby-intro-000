class Dog 
  def initialize(name, breed="Mutt")
    @name = name
    @breed = breed 
  end 
  
  def name 
    @name = name 
  end 
  
  def name=(this_name) 
    @name = this_name 
  end 
  
  def breed 
    @breed = breed 
  end 
  
  def breed=(this_breed) 
    @breed = this_breed 
  end 
end 
   