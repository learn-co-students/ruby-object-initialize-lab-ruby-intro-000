class Person
  def initialize(name)
    @name = name
  end
  def name                    #get name of Person
    @name
  end
  def name=(name)             #set name of Person
    @name = name
  end
  def job                     #get job of Person
    @job
  end
  def job=(job)               #set job of Person
    @job = job
  end
end